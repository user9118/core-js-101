/**
 * Возвращает сумму всех элементов в указанном массиве чисел
 *
 * @param {array} arr
 * @return {number}
 *
 * @example
 *   [] => 0
 *   [ 1, 2, 3 ]           => 6
 *   [ -1, 1, -1, 1 ]      => 0
 *   [ 1, 10, 100, 1000 ]  => 1111
 */
function getItemsSum(arr) {
  const result = arr.reduce((sum, el) => sum + el, 0);
  return result;
}

